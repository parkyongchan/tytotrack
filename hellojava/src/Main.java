public class Main {
    public static void main(String[] args) {
        int 사과 = 10;
        int 바나나 = 3;

        int 총과일 = 사과 + 바나나;
        int 차이 = 사과 - 바나나;
        int 곱하기 = 사과 * 바나나;
        int 나누기 = 사과 / 바나나;
        int 나머지 = 사과 % 바나나;

        System.out.println("사과: " + 사과);
        System.out.println("바나나: " + 바나나);
        System.out.println("사과 + 바나나 = " + 총과일);
        System.out.println("사과 - 바나나 = " + 차이);
        System.out.println("사과 × 바나나 = " + 곱하기);
        System.out.println("사과 ÷ 바나나 = " + 나누기);
        System.out.println("사과를 바나나로 나눈 나머지 = " + 나머지);


    }
}